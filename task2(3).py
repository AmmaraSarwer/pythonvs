# Initialize a variable
x = 10

# Assignment operators
print(f"Initial value: x = {x}")

x += 5  # x = x + 5
print(f"x += 5  -> {x}")

x -= 3  # x = x - 3
print(f"x -= 3  -> {x}")

x *= 2  # x = x * 2
print(f"x *= 2  -> {x}")

x /= 4  # x = x / 4
print(f"x /= 4  -> {x}")

x //= 2  # x = x // 2 (Floor division)
print(f"x //= 2 -> {x}")

x %= 3  # x = x % 3 (Modulus)
print(f"x %= 3  -> {x}")

x **= 2  # x = x ** 2 (Exponentiation)
print(f"x **= 2 -> {x}")